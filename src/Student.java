public class Student {
    private String ID,name,email,phone,address,avartar;
    private int sex;
    

    public Student(String ID, String name, String email, String phone, int sex, String address, String avartar) {
        this.ID = ID;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.sex = sex;
        this.address = address;
        this.avartar = avartar;
        
    }

    


    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getSex() {
        return sex;
    }

    public void setSex(int sex) {
        this.sex = sex;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAvartar() {
        return avartar;
    }

    public void setAvartar(String avartar) {
        this.avartar = avartar;
    }
    
}
